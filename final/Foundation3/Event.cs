public class Event
{
    string _title;
    string _description;
    string _date;
    string _time;
    string _address;

    public Event(string title, string description, string date, string time, string address)
    {
        _title       = title;
        _description = description;
        _date        = date;
        _time        = time;
        _address     = address;
    }
    
}